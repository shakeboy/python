str = "sophiscated"
print(str)                                               # 整个字符串.
print(str[1])                                             # 索引为1的字符.
print(str[2:])                                            # 从索引2开始的所有字符.
print(str[:])                                             # 整个字符串.
print(str[:4])                                            # 索引为4之前的所有字符.
print(str[-1])                                            # 倒数第1个字符.
print(str[-5:-1])                                          # 索引为-5~-2范围的字符.

